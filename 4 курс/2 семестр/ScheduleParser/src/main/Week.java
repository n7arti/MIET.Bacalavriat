package main;

public class Week {
    public Day mon;
    public Day tue;
    public Day wed;
    public Day thu;
    public Day fri;
    public Day sat;

    public Week(Day mon, Day tue, Day wed, Day thu, Day fri, Day sat) {
        this.mon = mon;
        this.tue = tue;
        this.wed = wed;
        this.thu = thu;
        this.fri = fri;
        this.sat = sat;
    }
}
